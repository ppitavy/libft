#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		l;
	int		i;

	l = ft_strlen((char *)s);
	if (!(str = malloc(sizeof(char) * (l + 1))))
		return (0);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
